package models;

public class Busca {
	private String text;
	private String tipo;
	
	public Busca() {
	}
	
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getTipo() {
		return tipo;
	}	
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
}
